package com.techlab.variabledemo;

public class VariableDemo {
	public static void main(String[] args) {
		int a = 10;
		float f = 10.5f;
		double d = 11.76;
		char c = 'A';
		String S = "Govind";
		System.out.println( a);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(S);
	}

}
