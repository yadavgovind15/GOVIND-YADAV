package com.techlab.variabledemo;

public class CharDemo {
	public static void main(String[] args) {
		char ch = 'b';
		switch (ch) {
		case 'a':
			System.out.println(" I am A ");
			break;
		case 'b':
			System.out.println(" I am B");
		default:
			System.out.println("I am default case");
			break;
		}
	}

}
